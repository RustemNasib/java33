package lesson_05;

public class Cycle_For_each1 {
    public static void main(String[] args) {
        System.out.println("Задача 1. Печать массива с помощью цикла for each");
        char[] symbol = {'d','4','!','A','f','h'};
        for(char ch:symbol){
            System.out.print(ch + "; ");
        }


    }
}
